// ── Helper: get all unique countries across all modes ────────────────────────

export function getAllCountries(): string[] {
  const set = new Set<string>();
  ITPS_RATES.forEach(r => set.add(r.country));
  EMS_RATES.forEach(r => set.add(r.country));
  CM_RATES.forEach(r => set.add(r.country));
  return Array.from(set).sort();
}

export function getItpsRate(country: string): ItpsRate | undefined {
  return ITPS_RATES.find(r => r.country.toLowerCase() === country.toLowerCase());
}

export function getEmsRate(country: string): EmsRate | undefined {
  return EMS_RATES.find(r => r.country.toLowerCase() === country.toLowerCase());
}

export function getCmRate(country: string): CmRate | undefined {
  return CM_RATES.find(r => r.country.toLowerCase() === country.toLowerCase());
}
