// Shipping rates for India Post international services (in INR)
// All rates are FROM India.

// ── Types ──────────────────────────────────────────────────────────────────────

export type ItpsRate = {
  country: string;
  first50gm: number;   // INR for first 50 gm (or part thereof)
  per50gm: number;     // INR per additional 50 gm (or part thereof)
  maxKg: number;       // Maximum weight limit
};

export type EmsRate = {
  country: string;
  first250gm: number;  // INR for first 250 gm (or part thereof)
  per250gm: number;    // INR per additional 250 gm (or part thereof)
};

export type CmRate = {
  country: string;
  first250gm: number;
  per250gm: number;
};

// ── Calculation helpers ─────────────────────────────────────────────────────

/** Compute ITPS cost for a given weight in grams */
export function calcItps(rate: ItpsRate, weightGm: number): number | null {
  if (weightGm > rate.maxKg * 1000) return null; // Exceeds max weight
  const additionalSlabs = Math.ceil(Math.max(weightGm - 50, 0) / 50);
  return rate.first50gm + additionalSlabs * rate.per50gm;
}

/** Compute EMS or CM cost for a given weight in grams */
export function calcEmsOrCm(rate: EmsRate | CmRate, weightGm: number): number {
  const additionalSlabs = Math.ceil(Math.max(weightGm - 250, 0) / 250);
  return rate.first250gm + additionalSlabs * rate.per250gm;
}

